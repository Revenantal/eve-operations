     */
    public function update(Request $request, $id)
    {
        $user = User::find($id);
        $roles = $request->roles;

        DB::table('role_user')->where('user_id', $id)->delete();

        foreach ($roles as $role){
            $user->attachRole($role);
        }

        Toastr::success("Roles Updated!");

        return redirect()->route('users.index');
    }

